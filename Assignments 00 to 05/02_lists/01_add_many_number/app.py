# 01_add_many_number

def sum_numbers(numbers):
    return sum(numbers)

def main():
    # List of numbers to be summed
    numbers = [1, 2, 3, 4, 5]
    
    # Call the function and store result
    result = sum_numbers(numbers)
    
    # Print the result
    print("The sum of the numbers is:", result)

# Entry point of the program
if __name__ == "__main__":
    main()
